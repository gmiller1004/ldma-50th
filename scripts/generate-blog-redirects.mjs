#!/usr/bin/env node
/**
 * Fetch myldma.com blog sitemap and generate Next.js redirects:
 * - Article URLs (/blogs/category/article-slug) → /blog/{slug}
 * - All other blog URLs (index, category index, duplicates) → /blog
 *
 * Writes lib/blog-redirects.generated.js (for next.config). Run when sitemap changes.
 * Usage: node scripts/generate-blog-redirects.mjs
 */
import { writeFileSync } from "fs";
import { fileURLToPath } from "url";
import { dirname, join } from "path";

const SITEMAP_URL = "https://myldma.com/sitemap_blogs_1.xml";

function parseSitemapXml(xml) {
  const entries = [];
  const urlBlocks = xml.match(/<url>[\s\S]*?<\/url>/gi) || [];
  for (const block of urlBlocks) {
    const loc = block.match(/<loc>([^<]+)<\/loc>/i)?.[1]?.trim();
    if (loc) entries.push({ loc });
  }
  return entries;
}

function isArticleUrl(loc) {
  try {
    const path = new URL(loc).pathname;
    const parts = path.split("/").filter(Boolean);
    return parts[0] === "blogs" && parts.length === 3;
  } catch {
    return false;
  }
}

function slugFromLoc(loc) {
  try {
    const path = new URL(loc).pathname;
    const segments = path.split("/").filter(Boolean);
    const last = segments[segments.length - 1] || "post";
    const decoded = decodeURIComponent(last);
    return decoded
      .toLowerCase()
      .replace(/[^a-z0-9-]/g, "-")
      .replace(/-+/g, "-")
      .replace(/^-|-$/g, "") || "post";
  } catch {
    return "post";
  }
}

function pathnameFromLoc(loc) {
  try {
    return new URL(loc).pathname;
  } catch {
    return "/blogs";
  }
}

async function main() {
  console.log("Fetching", SITEMAP_URL, "...");
  const res = await fetch(SITEMAP_URL);
  if (!res.ok) {
    console.error("Failed to fetch sitemap:", res.status);
    process.exit(1);
  }
  const xml = await res.text();
  const entries = parseSitemapXml(xml);

  const seen = new Set();
  const redirects = [];

  // Root blog index (sitemap may not list it)
  redirects.push({ source: "/blogs", destination: "/blog" });
  seen.add("/blogs");

  for (const { loc } of entries) {
    const pathname = pathnameFromLoc(loc);
    if (seen.has(pathname)) continue;
    seen.add(pathname);

    if (isArticleUrl(loc)) {
      const slug = slugFromLoc(loc);
      redirects.push({ source: pathname, destination: `/blog/${slug}` });
    } else {
      redirects.push({ source: pathname, destination: "/blog" });
    }
  }

  const __dirname = dirname(fileURLToPath(import.meta.url));
  const outPath = join(__dirname, "..", "lib", "blog-redirects.generated.js");
  const content = `// Generated by scripts/generate-blog-redirects.mjs — do not edit by hand.
// Re-run when myldma.com blog sitemap changes: node scripts/generate-blog-redirects.mjs

"use strict";
module.exports = { BLOG_REDIRECTS: ${JSON.stringify(redirects)} };
`;

  writeFileSync(outPath, content, "utf8");
  console.log("Wrote", redirects.length, "redirects to", outPath);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
